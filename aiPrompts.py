def get_starting_words_prompt(target_lang):
    """
    Генерирует промпт для получения 10 стартовых слов.
    Использует жесткий JSON-формат с универсальным ключом 'foreign' во избежание KeyError.
    """
    lang_name = "английский" if target_lang == "en" else "немецкий"
    return f"""
    Ты — ИИ-лексикограф. Твоя задача — выдать ровно 10 самых популярных, базовых и часто используемых слов (существительные, глаголы, прилагательные) на языке '{lang_name}' для начинающего уровня (A1-A2).

    🚨 КРИТИЧЕСКОЕ ТРЕБОВАНИЕ К ФОРМАТУ: 
    Ответ выдай СТРОГО в формате JSON-массива, без markdown-разметки (НЕ используй ```json), без вступлений и пояснений.
    Ключи в JSON должны называться СТРОГО "foreign" (для слова на языке {lang_name}) и "ru" (для перевода на русский). Другие названия ключей запрещены!

    Формат ответа:
    [
      {{"foreign": "слово_на_иностранном", "ru": "перевод_на_русский"}},
      {{"foreign": "слово_на_иностранном", "ru": "перевод_на_русский"}}
    ]
    """


def generate_pure_vocabulary_task_prompt(target_lang, difficulty, keywords, history=None):
    """
    Генерирует предложение на РУССКОМ языке сугубо на основе слов из словаря юзера,
    применяя изолированные жесткие критерии под каждый уровень сложности (A1-C1).
    """
    # Нормализуем уровень
    diff = str(difficulty).strip().upper().replace("А", "A")
    lang_name = "английский" if target_lang == "en" else "немецкий"

    # Форматируем ключевые слова, которые ИИ ОБЯЗАН использовать (через универсальный foreign)
    words_str = ", ".join([f'"{w["foreign"]}" ({w["ru"]})' for w in keywords])

    # Базовая инструкция, общая для всех уровней
    base_instruction = f"""Ты — профессиональный ИИ-ментор и лингвист. Твоя задача — придумать ОДНО предложение на РУССКОМ языке, которое при переводе на {lang_name} будет строго соответствовать уровню сложности '{diff}'.

🚨 КРИТИЧЕСКОЕ УСЛОВИЕ: Это предложение должно быть составлено так, чтобы при его переводе на {lang_name} ОБЯЗАТЕЛЬНО использовались следующие слова из личного словаря пользователя: {words_str}. Ты можешь менять их грамматическую форму (число, падеж, время для глаголов), но они обязаны быть смысловым центром предложения.
"""

    # Блок истории (запрет на повторы)
    history_condition = ""
    if history:
        history_lines = "\n".join([f"- {phrase}" for phrase in history])
        history_condition = f"""
🚨 ЗАПРЕТ НА ПОВТОРЫ: Следующие предложения на РУССКОМ языке уже были выданы пользователю сегодня. Тебе КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО генерировать их или их близкие синонимы:
{history_lines}
"""

    # Финальный хвост форматирования, общий для всех
    format_tail = f"""
Укажи конкретное грамматическое правило, которое тестируется, и обязательно ДОБАВЬ в скобках английские ключевые слова для этого правила.
Ответь СТРОГО в одну строку без приветствий, markdown-разметки (не используй ```json) и внешних кавычек.
Формат ответа строго такой JSON:
{{"phrase": "Фраза на русском", "translation": "Эталонный перевод на {lang_name}", "rule_hint": "Название правила (english keywords)"}}"""

    # 🚨 ИЗОЛИРОВАННЫЙ БЛОК: УРОВЕНЬ А1
    if diff == "A1":
        level_rules = f"""
📋 ЖЕСТКИЕ ТРЕБОВАНИЯ К УРОВНЮ А1:
1. Длина фразы строго от 3 до 5 слов! Длинные предложения категорически запрещены.
2. Только примитивная базовая бытовая лексика: поиск вещей, простые вопросы в городе, заказ в кафе (например: 'Где мой телефон?', 'Сколько это стоит?', 'Я хочу чаю.').
3. Категорически запрещено использовать союзы 'потому что' (because), 'хотя', 'если' и цепочки глаголов.
4. Не используй темы работы, дедлайнов, созвонов и сложных технологий.
"""
        return base_instruction + level_rules + history_condition + format_tail

    # 🚨 ИЗОЛИРОВАННЫЙ БЛОК: УРОВЕНЬ А2
    elif diff == "A2":
        level_rules = f"""
📋 ЖЕСТКИЕ ТРЕБОВАНИЯ К УРОВНЮ А2:
1. Длина предложения строго от 5 до 8 слов. Никаких громоздких и длинных конструкций!
2. Предложение должно быть простым (одна законченная мысль). Категорически запрещено разделять его запятыми на две части и использовать сложные союзы 'потому что' (because), 'хотя', 'так как'.
3. Контекст должен быть максимально ходовым и жизненным: простые ситуации в путешествии, заказ еды, покупка билетов, короткий рассказ о планах или о том, что произошло вчера.
4. Разрешенная грамматика: Past Simple (что делал вчера), Future Simple (will), базовые модальные глаголы (can, must, should), простые предлоги времени (in, at, on).
5. Примеры идеального построения: 'Вчера я купил новый телефон.', 'Ты должен позвонить мне завтра.', 'Я не могу найти свой паспорт.'
"""
        return base_instruction + level_rules + history_condition + format_tail

    # 🚨 ИЗОЛИРОВАННЫЙ БЛОК: УРОВЕНЬ B1
    elif diff == "B1":
        level_rules = f"""
📋 ЖЕСТКИЕ ТРЕБОВАНИЯ К УРОВНЮ B1:
1. Длина предложения строго от 8 до 12 слов. Оно должно быть емким, без лишней 'воды' и тройных придаточных конструкций.
2. Контекст: современные технологии (завис роутер, пропал интернет), рабочие моменты (дедлайны, созвоны, простые факапы), путешествия или повседневный быт.
3. ОБЯЗАТЕЛЬНО заложи в фразу использование ОДНОГО ходового разговорного выражения или фразового глагола, подходящего по смыслу к твоим ключевым словам.
4. Разрешенная грамматика: Present Perfect, пассивный залог, простые условные предложения (If + Present Simple, will...), герундий или инфинитив.
5. Примеры идеального построения: 'Я только что выяснил, почему наш сайт не работает.', 'Если мы не поторопимся, мы точно сорвем этот дедлайн.', 'Эта проблема должна быть решена до начала созвона.'
"""
        return base_instruction + level_rules + history_condition + format_tail

    # 🚨 ИЗОЛИРОВАННЫЙ БЛОК: УРОВЕНЬ B2
    elif diff == "B2":
        level_rules = f"""
📋 ТРЕБОВАНИЯ К УРОВНЮ B2:
1. Длина предложения от 12 до 16 слов. Оно должно имитировать живую речь профессионала или уверенного путешественника.
2. Контекст: рабочие факапы, серьезные технические или организационные проблемы, споры, переговоры, обсуждение рисков (например, падение серверов, срыв контрактов, автономные поездки).
3. ОБЯЗАТЕЛЬНО заложи в фразу продвинутые бизнес-выражения или фразовые глаголы (например: 'отложить/put off', 'учесть риски/take risks into account').
4. Обязательная грамматика: Past Perfect, сложные формы пассивного залога (Continuous/Perfect Passive), модальные глаголы в прошлом (should have done), Conditionals 2 или 3 типа.
5. Примеры идеального построения: 'Встречу пришлось отменить, потому что команда разработчиков ещё не подготовила финальный отчёт.', 'Тебе следовало предупредить меня о сбое в сети до того, как клиенты начали жаловаться.'
"""
        return base_instruction + level_rules + history_condition + format_tail

    # 🚨 ИЗОЛИРОВАННЫЙ БЛОК: УРОВЕНЬ C1 (ПО УМОЛЧАНИЮ)
    else:
        level_rules = f"""
📋 ТРЕБОВАНИЯ К УРОВНЮ C1:
1. Длина от 15 до 20 слов. Фраза должна звучать аутентично, как из качественного сериала, подкаста или статьи в Harvard Business Review.
2. Контекст: тонкая ирония, абстрактные концепции, глубокий анализ ситуации, профессиональный кризис-менеджмент, психологические оттенки в быту.
3. ОБЯЗАТЕЛЬНО используй редкие идиомы, метафоры или тонкие устойчивые выражения (например: 'читать между строк', 'быть в тупике/be at a loss').
4. Сложная грамматика: Смешанные условные предложения (Mixed Conditionals), инверсия (Not only... but also), инфинитивные и герундиальные обороты повышенной сложности, конструкции с Wish / Ирреальное наклонение.
5. Примеры идеального построения: 'Редко когда проект продвигается так гладко, учитывая, сколько скрытых препятствий нам пришлось преодолеть в самом начале.', 'Едва я успел разобраться с одной проблемой в коде, как вся система развалилась прямо у меня на глазах.'
"""
        return base_instruction + level_rules + history_condition + format_tail


def word_translation_prompt(word, target_lang):
    lang_name = "английский" if target_lang == "en" else "немецкий"
    return (
        f"Ты — точный электронный словарь. Переведи слово или короткую фразу '{word}' на {lang_name} язык.\n"
        f"Если слово написано на русском, переведи его на {lang_name}.\n"
        f"Если слово уже написано на {lang_name}, переведи его на русский.\n\n"
        f"Ответь СТРОГО в одну строку в формате: Оригинал || Перевод\n"
        f"Никаких пояснений, транскрипций и кавычек. Пример: уязвимый || vulnerable"
    )


def answer_prompt(original_ru, user_answer, user_config):
    """Промпт для проверки перевода грамматической фразы пользователя"""
    lang_name = "английский" if user_config.get("source_lang") == "en" else "немецкий"
    difficulty = user_config.get("difficulty", "A1")

    return (
        f"Ты — строгий, но поддерживающий ИИ-Ментор по языку. Твоя задача — проверить перевод пользователя.\n"
        f"Оригинальная фраза на русском: '{original_ru}'\n"
        f"Пользователь перевел на {lang_name} язык: '{user_answer}'\n"
        f"Целевой уровень сложности: {difficulty}\n\n"
        f"Проанализируй ответ. Если перевод верный и грамматически точный для уровня {difficulty}, начни свой ответ строго с префикса '✅'.\n"
        f"Если в переводе есть критические ошибки, опечатки или он не соответствует правилу, начни ответ строго с префикса '❌'.\n\n"
        f"Дай короткий, понятный разбор на русском языке: укажи на ошибки (если они есть), предложи идеальный вариант и похвали за правильные моменты. Напиши лаконично, без лишней воды."
    )


def help_prompt(original_ru, user_config):
    """Промпт для первой подсказки (наводящие вопросы, намеки на структуру)"""
    lang_name = "английский" if user_config.get("source_lang") == "en" else "немецкий"
    return (
        f"Ты — поддерживающий языковой ментор. Пользователь пытается перевести фразу: '{original_ru}' на {lang_name}.\n"
        f"Он попросил первую подсказку. Не давай ему готовый перевод!\n"
        f"Вместо этого дай намек: напомни порядок слов, укажи на начальные буквы ключевых слов или напомни, "
        f"какой вспомогательный глагол нужно использовать. Напиши коротко, буквально 2-3 строки на русском."
    )


def final_ready_prompt(original_ru, user_config):
    """Промпт для финального ответа, когда попытки подсказок кончились"""
    lang_name = "английский" if user_config.get("source_lang") == "en" else "немецкий"
    return (
        f"Ты — учитель языка. Пользователь не смог перевести фразу: '{original_ru}' на {lang_name} и потратил все подсказки.\n"
        f"Напиши ему эталонный, идеальный перевод этой фразы на {lang_name}.\n"
        f"Ниже дай супер-короткое (1-2 предложения) пояснение грамматики, почему именно так строится это предложение. Без лишней воды."
    )


